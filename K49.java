public class K49{
    public static void main(String[] args){
	int n1=5;
	int n2=3;
	do {
	    System.out.println("Line: "+n1);
	    n1--;}
	    while(n1>0);

	do {
	    System.out.println("New Line: "+n2);}
	    while(--n2>0);
    }
}
